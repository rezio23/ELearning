$("#searchCourse").on("keyup", function () {

  let searchValue = $(this).val().toLowerCase().trim();

  $(".lesson").each(function () {

    let title = $(this).data("title").toLowerCase();

    if (title.includes(searchValue)) {
      $(this).show();
    } else {
      $(this).hide();
    }

  });

});